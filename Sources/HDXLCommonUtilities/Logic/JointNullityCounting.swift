import Foundation

// -------------------------------------------------------------------------- //
// MARK: Joint-`nil` Counting
// -------------------------------------------------------------------------- //

/// Returns the count of non-`nil` values passed into it.
@inlinable
public func countOfNilStatus(
  _ a: Any?,
  _ b: Any?) -> JointNullityCount {
  return JointNullityCount(
    withCountsForValues: a,b
  )
}

/// Returns a simultaneous count of the `nil` and non-`nil` values passed into it.
@inlinable
public func countOfNilStatus(
  _ a: Any?,
  _ b: Any?,
  _ c: Any?) -> JointNullityCount {
   return JointNullityCount(
     withCountsForValues: a,b,c
  )
}

