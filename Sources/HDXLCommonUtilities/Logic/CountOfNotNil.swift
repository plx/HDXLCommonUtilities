/// Returns the count of non-`nil` values passed into it.
@inlinable
public func countOfNonNil(
  _ a: Any?,
  _ b: Any? ) -> Int {
  return (
    a.oneIfNotNil
    +
    b.oneIfNotNil
  )
}

